#include <iostream>

int main()
{
    // Membuat perulangan for
    /*
        perulangan for :
        perulangan yang kondisi nya dapat ditulis dalam satu baris
    */

    // int i = variabel (nilai awal)
    // i <= 5 = kondisi yang harus terpenuhi
    // i++ = increment (menambahkan nilai tiap perulangan)

    for (int i = 1; i <= 5; i++) // ditulis dalam satu baris
    {
        std::cout << "Hello World\n"; // menampilkan data hello world sebanyal 5x
    }

    return 0;
}